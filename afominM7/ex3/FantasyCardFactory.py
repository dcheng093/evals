from ex3.CardFactory import CardFactory
from ex0.CreatureCard import CreatureCard
from ex1.SpellCard import SpellCard
from ex1.ArtifactCard import ArtifactCard


class FantasyCardFactory(CardFactory):
    def create_creature(self, name) -> CreatureCard:
        if name.lower() == "dragon":
            return CreatureCard("Fire Dragon", 5, "Legendary", 7, 5)
        return CreatureCard("Goblin Warrior", 2, "Common", 2, 1)

    def create_spell(self, name) -> SpellCard:
        if name.lower() == "lightning bolt":
            return SpellCard("Lightning Bolt", 3, "Common", "damage")

    def create_artifact(self, name) -> ArtifactCard:
        if name.lower() == "mana ring":
            return ArtifactCard("Mana Ring", 2, "Rare", 5, "+1 mana per turn")

    def create_themed_deck(self, size: int) -> dict:
        return {"deck_size": size, "theme": "Fantasy"}

    def get_supported_types(self) -> dict:
        return {
            'creatures': ['dragon', 'goblin'],
            'spells': ['fireball'],
            'artifacts': ['mana_ring']
        }
