#include <unistd.h>

int	main(int ac, char **av)
{
	char	*str;
	int		i;

	if (ac != 1)
		return (0);
	str = av[0];
	i = 0;
	while (str[i])
		write(1, &str[i++], 1);
	write(1, "\n", 1);
}
