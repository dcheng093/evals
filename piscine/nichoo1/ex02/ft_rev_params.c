#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		write(1, &str[i++], 1);
}

int	main(int ac, char **av)
{
	int	i;

	if (ac == 1)
		return (0);
	i = ac - 1;
	while (i > 0)
	{
		ft_putstr(av[i--]);
		ft_putchar('\n');
	}
}
